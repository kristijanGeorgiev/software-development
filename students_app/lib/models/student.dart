class Student {
  final String id;
  final String name;
  final String surname;
  final String email;
  final String address;
  final int credits;
  final DateTime date;
  Student({
    required this.id,
    required this.name,
    required this.surname,
    required this.email,
    required this.address,
    required this.credits,
    required this.date,
});
}